package Lab_06;

public class Step04ScapeCharacter {

    public static void main(String[] args) {
        // Escape characters are used to represent single characters in a string
        // They are preceded by a backslash (\)

        System.out.println("This is a double quote: \"");
        System.out.println("This is a single quote: \'");
        System.out.println("This is a backslash: \\");
        System.out.println("This is a tab: \t and this after tab");
        System.out.println("This is a newline: \n and this after newline");
        System.out.println(
                "This is a carriage return--------------------------------: \r and this after carriage return");
        System.out.println("This is a form feed: \f and this after form feed");
        System.out.println("This is a unicode character: \u00A9");
        System.out.println("This is a unicode character: \u00AE");

    }

}
