package Lab_04;

public class Step01Types {
    public static void main(String[] args) {
        // Primitive Data Types
        boolean isTrue = true;
        char character = 'A';
        byte byteNumber = 127;
        short shortNumber = 32767;
        int integerNumber = 2147483647;
        long longNumber = 9223372036854775807L;
        float floatNumber = 3.4028235E38f;
        double doubleNumber = 1.7976931348623157E308;

        System.out.println("Boolean: " + isTrue);
        System.out.println("Character: " + character);
        System.out.println("Byte: " + byteNumber);
        System.out.println("Short: " + shortNumber);
        System.out.println("Integer: " + integerNumber);
        System.out.println("Long: " + longNumber);
        System.out.println("Float: " + floatNumber);
        System.out.println("Double: " + doubleNumber);

    }
}
